package task.week1;

/**
 * **Task 1:** Write a program that declares variables of different data types
 * (int, float, double, char, String, boolean) and prints
 */
public class W1t1 {
    public static void main(String[] args) {
        int i = 0;
        float f = 0.1f;
        double d = 1.234;
        char c = 'c';
        String s = "sample";
        boolean b = false;
        System.out.println("int i: " + i
                         + ", float f: " + f
                         + ", double d: " + d
                         + ", char c: " + c
                         + ", String s: " + s
                         + ", boolean b: " + b);
    }
}

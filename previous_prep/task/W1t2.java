package task.week1;

/**
 * **Task 2:** Implement a simple calculator that can perform basic arithmetic operations (+, -, *, /).
 */
public class W1t2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 2;

        System.out.println("Simple Calculator");
        System.out.println(a + " + " + b + " = " + (a + b));
        System.out.println(a + " - " + b + " = " + (a - b));
        System.out.println(a + " * " + b + " = " + (a * b));
        System.out.println(a + " / " + b + " = " + (a / b));
    }
}
